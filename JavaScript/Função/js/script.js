// tipos de funções

// função declarativa:
function funcao(){
    console.log("Sou uma mensagem de uma função declarativa")
}

funcao()

// função expressiva:
var funcaoExpressiva = function(){
        console.log("Sou uma mensagem de uma função expressiva ")
}

funcaoExpressiva()

// Arrow function:
var arrowFunction = () =>{
    console.log("Sou uma arrow function")
}

arrowFunction()


